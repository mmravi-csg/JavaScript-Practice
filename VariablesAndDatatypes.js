// Define variables
let num1 = 10;
let num2 = 5;
let str = "Hello, World!";
let bool = true;
let arr = [1, 2, 3, 4, 5];
let obj = { key: "value" };

// Display variable values and types
console.log("num1:", num1, typeof num1);
console.log("num2:", num2, typeof num2);
console.log("str:", str, typeof str);
console.log("bool:", bool, typeof bool);
console.log("arr:", arr, typeof arr);
console.log("obj:", obj, typeof obj);

// Arithmetic operators
console.log("Addition:", num1 + num2);
console.log("Subtraction:", num1 - num2);
console.log("Multiplication:", num1 * num2);
console.log("Division:", num1 / num2);
console.log("Modulus:", num1 % num2);

// Comparison operators
console.log("Equal to:", num1 == num2);
console.log("Not equal to:", num1 != num2);
console.log("Greater than:", num1 > num2);
console.log("Less than:", num1 < num2);
console.log("Greater than or equal to:", num1 >= num2);
console.log("Less than or equal to:", num1 <= num2);

// Logical operators
console.log("Logical AND:", bool && true);
console.log("Logical OR:", bool || false);
console.log("Logical NOT:", !bool);

// Typeof operator
console.log("Type of str:", typeof str);
console.log("Type of num1:", typeof num1);
